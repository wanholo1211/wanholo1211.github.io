function overlayOn(workID, videoNum) {
  var target = document.getElementById("overlayContent");
  target.innerHTML += content[workID];


  document.getElementById("overlay").style.display = "block";
  document.getElementById("body").style.overflow = "hidden";

  for (var i = 0; i < videoNum; i++){
    var videoName = document.getElementById("work"+ workID +"_vid" + i);
    videoName.muted = true;
    videoName.currentTime = 0;
    videoName.play();
  }
}

function overlayOff() {
  var target = document.getElementById("overlayContent");
  target.innerHTML = "";
  document.getElementById("overlay").style.display = "none";
  document.getElementById("body").style.removeProperty("overflow");
}